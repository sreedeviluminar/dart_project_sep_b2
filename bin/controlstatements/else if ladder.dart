void main() {
  String size = 'XXXL';

  if (size == 'XS') {
    print('Size is Xs');
  } else if (size == 'XL') {
    print('Size is XL');
  } else if (size == 'S') {
    print('Size is s');
  } else if (size == 'M') {
    print('Size is M');
  } else if (size == 'L') {
    print('Size is L');
  } else if (size == 'XXL') {
    print('Size is XLL');
  }else{
    print('Size is not available');

  }
}
